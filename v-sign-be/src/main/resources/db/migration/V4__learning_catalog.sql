create table learning_units (
    unit_id varchar(80) primary key,
    title varchar(160) not null,
    description text,
    thumbnail_url text,
    is_published boolean not null default true,
    order_index int not null,
    created_at timestamp with time zone not null default now(),
    updated_at timestamp with time zone not null default now()
);

create table learning_chapters (
    chapter_id varchar(80) primary key,
    unit_id varchar(80) not null references learning_units(unit_id),
    title varchar(160) not null,
    description text,
    is_premium boolean not null default false,
    is_published boolean not null default true,
    order_index int not null,
    created_at timestamp with time zone not null default now(),
    updated_at timestamp with time zone not null default now()
);

create table learning_lessons (
    lesson_id varchar(80) primary key,
    chapter_id varchar(80) not null references learning_chapters(chapter_id),
    title varchar(160) not null,
    description text,
    video_url text,
    duration_seconds int not null default 0,
    is_premium boolean not null default false,
    is_published boolean not null default true,
    order_index int not null,
    created_at timestamp with time zone not null default now(),
    updated_at timestamp with time zone not null default now()
);

create table practice_items (
    practice_item_id varchar(100) primary key,
    lesson_id varchar(80) not null references learning_lessons(lesson_id),
    category varchar(60) not null,
    level varchar(30) not null,
    label varchar(160) not null,
    expected_gloss varchar(120) not null,
    source_video_file varchar(120),
    is_published boolean not null default true,
    order_index int not null,
    created_at timestamp with time zone not null default now(),
    updated_at timestamp with time zone not null default now()
);

create table practice_item_rubrics (
    id int generated by default as identity primary key,
    practice_item_id varchar(100) not null references practice_items(practice_item_id),
    code varchar(80) not null,
    order_index int not null
);

create table lesson_progress (
    id int generated by default as identity primary key,
    user_key varchar(255) not null,
    lesson_id varchar(80) not null references learning_lessons(lesson_id),
    completion_pct int not null default 0,
    last_position_seconds int not null default 0,
    phase varchar(40) not null default 'VIDEO',
    current_question_index int,
    status varchar(40) not null default 'NOT_STARTED',
    created_at timestamp with time zone not null default now(),
    updated_at timestamp with time zone not null default now(),
    unique (user_key, lesson_id)
);

