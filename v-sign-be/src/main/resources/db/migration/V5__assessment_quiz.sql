create table assessments (
    assessment_id varchar(100) primary key,
    title varchar(180) not null,
    passing_score int not null,
    is_published boolean not null default true,
    order_index int not null,
    created_at timestamp with time zone not null default now(),
    updated_at timestamp with time zone not null default now()
);

create table assessment_questions (
    question_id varchar(100) primary key,
    assessment_id varchar(100) not null references assessments(assessment_id),
    prompt varchar(255) not null,
    correct_answer_id varchar(100) not null,
    order_index int not null
);

create table assessment_options (
    id int generated by default as identity primary key,
    question_id varchar(100) not null references assessment_questions(question_id),
    option_id varchar(100) not null,
    text varchar(160) not null,
    order_index int not null
);

create table assessment_submissions (
    id int generated by default as identity primary key,
    assessment_id varchar(100) not null references assessments(assessment_id),
    user_id varchar(120) not null,
    score int not null,
    passed boolean not null,
    correct_answers int not null,
    total_questions int not null,
    awarded_xp int not null,
    created_at timestamp with time zone not null default now()
);

create table assessment_submission_answers (
    id int generated by default as identity primary key,
    submission_id int not null references assessment_submissions(id),
    question_id varchar(100) not null,
    selected_answer_id varchar(100),
    correct boolean not null
);

create table lesson_quizzes (
    quiz_id varchar(100) primary key,
    lesson_id varchar(100) not null,
    title varchar(180) not null,
    passing_score int not null,
    xp_award int not null,
    requires_premium boolean not null default false,
    is_published boolean not null default true,
    created_at timestamp with time zone not null default now(),
    updated_at timestamp with time zone not null default now()
);

create table quiz_questions (
    question_id varchar(100) primary key,
    quiz_id varchar(100) not null references lesson_quizzes(quiz_id),
    prompt varchar(255) not null,
    correct_answer_id varchar(100) not null,
    order_index int not null
);

create table quiz_options (
    id int generated by default as identity primary key,
    question_id varchar(100) not null references quiz_questions(question_id),
    answer_id varchar(100) not null,
    text varchar(160) not null,
    order_index int not null
);

create table quiz_attempts (
    attempt_id varchar(120) primary key,
    quiz_id varchar(100) not null references lesson_quizzes(quiz_id),
    lesson_id varchar(100) not null,
    submitted boolean not null default false,
    score int not null default 0,
    passed boolean not null default false,
    duration_seconds int not null default 0,
    timed_out boolean not null default false,
    created_at timestamp with time zone not null default now(),
    submitted_at timestamp with time zone
);

create table quiz_attempt_answers (
    id int generated by default as identity primary key,
    attempt_id varchar(120) not null references quiz_attempts(attempt_id),
    question_id varchar(100) not null,
    selected_answer_id varchar(100),
    correct boolean not null
);

